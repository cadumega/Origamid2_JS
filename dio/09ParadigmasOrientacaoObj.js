// Abstracao isolar um aspecto determinado, do complexo tornar mais simples. Abstrair é generalizar mais.
// ex: Especificar deixa as coisas mais complexas e individuais, exemplo do carro, que é veiculo de rodas, que é veiculo. barco é veiculo mas nao de rodas..
// Um celular pode fazer muitas coisas como ligar, tirar fotos, jogar. Mas nao mostra para você todo o processo interno, como as coisas funcionam essa implementacao fica oculta, precisariamos abstrair para extrair e ir simplificando toda esse funcionamento.

// Heranca - objeto filho herda propriedades e metodos do pai, mas os filhos tem as proprias caracteristicas.

//  Encapsulamento cada classe tem propriedades e métodos independentes do restante do codigo. Se a propriedade mudar de valor, irá apenas mudar  determinada classe e não de todo o código. Setores de uma empresa é um caso de escapsulamento, acoes especificas do setor.

//  Polimorfismo, podemos herdade a mesma classe pai, mas se comportam de forma diferente quando invocamos seus métodos. A classe pai éo animal, que tem o metodo falar, mas quando o gato, cachorro e pato falam, falam de forma diferente. Isso é o polimorfismo, se comportam de maneira difernetes quando chamamos os métodos deles.
//  Uma mulher pode ser muitas coisas mae, escritora, estudante, uma unica pessoa pode ter diferentes caminhos.

